    b: uint8

  InnerUnionKind = enum
    iuA, iuB

  InnerUnion = object
    case kind: InnerUnionKind
    of iuA:
      va: InnerA
    of iuB:
      vb: InnerB

  OuterX = object
    x: uint64
  OuterY = object
    y: seq[byte]

  OuterUnionKind = enum
    ouX, ouY, ouInner

  OuterUnion = object
    case kind: OuterUnionKind
    of ouX:
      ox: OuterX
    of ouY:
      oy: OuterY
    of ouInner:
      inner: InnerUnion

